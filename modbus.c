void mb_poll(void)
{
	uint8_t i=0;
	switch(mb_t.mb_state)
	{
		case MB_ANSWER:
			mb_t.us1=256*mb_t.rx[2]+mb_t.rx[3];
			mb_t.us2=256*mb_t.rx[4]+mb_t.rx[5];
			switch(mb_t.rx[1])
			{
				case 0x01:
					mb_response_01();break;
				case 0x03:
					mb_response_03();break;
				case 0x05:
					mb_response_05();break;
				case 0x06:
					mb_response_06();break;
				case 0x0f:
					mb_response_15();break;	
				case 0x10:
					mb_response_16();break;									
			}
			//pcrc=((uint16_t*)&mb_t.tx[mb_t.tx_len-2]);
			(*((uint16_t*)&mb_t.tx[mb_t.tx_len-2]))=CRC16(mb_t.tx,mb_t.tx_len-2);
			mb_t.mb_state=MB_SEND;
		break;
	case MB_SEND:
		
		for(i=0;i<mb_t.tx_len;i++)
		{
			SendData(mb_t.tx[i]);
		}
		mb_t.mb_state=MB_READY;
		break;	
	}
}

