static uint16_t CRC16( uint8_t * pucFrame, uint16_t usLen )

{

    uint8_t           ucCRCHi = 0xFF;

    uint8_t           ucCRCLo = 0xFF;

    int16_t             iIndex;



    while( usLen-- )

    {

        iIndex = ucCRCLo ^ *( pucFrame++ );

        ucCRCLo = ( uint8_t )( ucCRCHi ^ aucCRCHi[iIndex] );

        ucCRCHi = aucCRCLo[iIndex];

    }

    return ( uint16_t )( ucCRCHi << 8 | ucCRCLo );

}

uint8_t read_bit_from_byte_stream(uint8_t *arr,uint16_t index)
{
	uint8_t x,y;
	y=index/8;
	x=index%8;
	return arr[y]&(1<<(x));
}

void write_bit_to_byte_stream(uint8_t *arr,uint16_t index,uint8_t value)
{
	uint8_t x,y;
	y=index/8;
	x=index%8;
	x=(1<<x);
	if(value) arr[y]|=x;
	else arr[y]&=~x;
}

void mb_bits_move(uint8_t *d,uint16_t d_start,uint8_t *s,uint16_t s_start,uint16_t n)
{
	uint16_t i=0;
	for(i=0;i<n;i++)
	{
		write_bit_to_byte_stream(d,d_start+i,read_bit_from_byte_stream(s,s_start+i));
	}
}

void mb_reg_move(uint16_t *d,uint16_t *s,uint16_t n)
{
	uint8_t i=0;
	n&=0x00ff;
	for(i=0;i<n;i++)
	{
		d[i]=rev16(s[i]);	
	}
}

static void mb_response_03()
{	
	mb_t.tx[0]=SLAVE_ID;
	mb_t.tx[1]=0x03;
	mb_t.tx[2]=mb_t.us2*2;
	mb_reg_move((uint16_t*)(&mb_t.tx[3]),mb_t.regs+mb_t.us1,mb_t.us2);
	mb_t.tx_len=5+mb_t.tx[2];
	
}

static void mb_response_16()
{
	memcpy(mb_t.tx,mb_t.rx,6);
	mb_reg_move(&mb_t.regs[mb_t.us1],(uint16_t*)(&mb_t.rx[7]),mb_t.us2);
	mb_t.tx_len=8;
	
}

static void mb_response_06()
{
	mb_t.regs[mb_t.us1]=mb_t.us2;
	mb_t.tx_len=8;
	memcpy(mb_t.tx,mb_t.rx,6);
}

static void mb_response_01()
{
	mb_t.tx[0]=SLAVE_ID;
	mb_t.tx[1]=0x01;
	mb_t.tx[2]=mb_t.us2/8;
	if(mb_t.us2%8) mb_t.tx[2]+=1;
	mb_t.tx[2+mb_t.tx[2]]=0;
	mb_bits_move(&mb_t.tx[3],0,mb_t.coils,mb_t.us1,mb_t.us2);
	mb_t.tx_len=5+mb_t.tx[2];
	
}


static void mb_response_05()
{
	if(mb_t.us2==0xff00) write_bit_to_byte_stream(mb_t.coils,mb_t.us1,1);
	else if (mb_t.us2==0x0000) write_bit_to_byte_stream(mb_t.coils,mb_t.us1,0);
	memcpy(mb_t.tx,mb_t.rx,6);
	mb_t.tx_len=8;
}

static void mb_response_15()
{
	mb_bits_move(mb_t.coils,mb_t.us1,&mb_t.rx[7],0,mb_t.us2);
	memcpy(mb_t.tx,mb_t.rx,6);
	mb_t.tx_len=8;
	
}

